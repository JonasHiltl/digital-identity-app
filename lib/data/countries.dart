List<CustomCountry> getCountrySuggestion(String query) {
  return countries
      .where(
          (country) => country.name.toLowerCase().contains(query.toLowerCase()))
      .toList();
}

CustomCountry getCountryByCode(String code) {
  return countries.firstWhere(
    (country) => country.code.toLowerCase().contains(
          code.toLowerCase(),
        ),
  );
}
